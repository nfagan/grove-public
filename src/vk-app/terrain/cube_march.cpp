Vec3f corner_coord(int i) {
  static const Vec3f coords[8]{
    Vec3f{},
    Vec3f{1, 0, 0},
    Vec3f{1, 0, 1},
    Vec3f{0, 0, 1},
    Vec3f{0, 1, 0},
    Vec3f{1, 1, 0},
    Vec3f{1, 1, 1},
    Vec3f{0, 1, 1},
  };
  assert(i < 8);
  return coords[i];
}

Vec3f coord_to_world(const GridInfo& grid, const Vec3f& c) {
  return grid.offset + grid.scale * c;
}

auto surface_estimate(float a, float b, float th) {
  struct Result {
    bool swapped;
    float f;
  };

  Result result{};

  result.swapped = a > b;
  if (result.swapped) {
    std::swap(a, b);
  }

  const float den = b - a;
  if (den == 0.0f) {
    result.f = 0.5f;
  } else {
    const float f = (th - a) / den;
    assert(f >= 0.0f && f <= 1.0f);
    result.f = f;
  }

  return result;
}

#if GROVE_CUBE_MARCH_INCLUDE_NORMALS
Vec3f sample_normal(const Vec3f& c, const GridInfo& grid, const GenSurface& gen_surface) {
  constexpr auto x = ConstVec3f::positive_x;
  constexpr auto y = ConstVec3f::positive_y;
  constexpr auto z = ConstVec3f::positive_z;
  auto dx = gen_surface(coord_to_world(grid, c + x)) - gen_surface(coord_to_world(grid, c - x));
  auto dy = gen_surface(coord_to_world(grid, c + y)) - gen_surface(coord_to_world(grid, c - y));
  auto dz = gen_surface(coord_to_world(grid, c + z)) - gen_surface(coord_to_world(grid, c - z));
  return normalize(Vec3f{dx, dy, dz});
}
#endif

GridSample sample_grid(const Vec3f& cube, const GridInfo& grid, const GenSurface& gen_surface,
                       float thresh) {
  GridSample result{};
  result.thresh = thresh;
  for (uint8_t i = 0; i < 8; i++) {
    const auto base_coord = cube + corner_coord(i);
    float sample = gen_surface(coord_to_world(grid, base_coord));
    if (sample < thresh) {
      result.below_surface |= (1 << i);
    }
    result.samples[i] = sample;
#if GROVE_CUBE_MARCH_INCLUDE_NORMALS
    result.normals[i] = sample_normal(base_coord, grid, gen_surface);
#endif
  }
  return result;
}

Vec3f surface_position(float t, int i0, int i1, const Vec3f& c, const GridInfo& grid) {
  return lerp(
    t,
    coord_to_world(grid, c + corner_coord(i0)),
    coord_to_world(grid, c + corner_coord(i1)));
}

GenTrisResult gen_tris(const Vec3f& cube, const GridInfo& grid, const GridSample& sample,
                       const GenTrisParams& params) {
  GenTrisResult result{};
  const int* edge_inds = triangulation[sample.below_surface];

  const bool smooth = params.smooth;
  int i{};
  while (i < 16 && edge_inds[i] >= 0) {
    const int ei0 = edge_inds[i];
    const int ei1 = edge_inds[i + 1];
    const int ei2 = edge_inds[i + 2];

    int a0 = corner_index_a[ei0];
    int a1 = corner_index_b[ei0];
    int b0 = corner_index_a[ei1];
    int b1 = corner_index_b[ei1];
    int c0 = corner_index_a[ei2];
    int c1 = corner_index_b[ei2];

    float ta = 0.5f;
    float tb = 0.5f;
    float tc = 0.5f;
    if (smooth) {
      auto resa = surface_estimate(sample.samples[a0], sample.samples[a1], sample.thresh);
      ta = resa.f;
      if (resa.swapped) {
        std::swap(a0, a1);
      }

      auto resb = surface_estimate(sample.samples[b0], sample.samples[b1], sample.thresh);
      tb = resb.f;
      if (resb.swapped) {
        std::swap(b0, b1);
      }

      auto resc = surface_estimate(sample.samples[c0], sample.samples[c1], sample.thresh);
      tc = resc.f;
      if (resc.swapped) {
        std::swap(c0, c1);
      }
    }

    auto* dst_p = result.positions + result.num_triangles * 3;
    *dst_p++ = surface_position(ta, a0, a1, cube, grid);
    *dst_p++ = surface_position(tb, b0, b1, cube, grid);
    *dst_p++ = surface_position(tc, c0, c1, cube, grid);

#if GROVE_CUBE_MARCH_INCLUDE_NORMALS
    auto* dst_n = result.normals + result.num_triangles * 3;
    *dst_n++ = normalize(lerp(ta, sample.normals[a0], sample.normals[a1]));
    *dst_n++ = normalize(lerp(tb, sample.normals[b0], sample.normals[b1]));
    *dst_n++ = normalize(lerp(tc, sample.normals[c0], sample.normals[c1]));
#endif

    result.num_triangles++;
    i += 3;
  }

  return result;
}


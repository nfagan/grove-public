Vec3f cm::world_to_coord(const Vec3f& p, const GridInfo& grid) {
  auto res = (p - grid.offset) / grid.scale;
  assert(floor(res) == res);
  return res;
}

void cm::simple_grid_march_range(const GridInfo& grid, const GenSurface& gen_surface,
                                 float thresh, const Vec3<int>& p0, const Vec3<int>& p1,
                                 const GenTrisParams& params,
                                 std::vector<Vec3f>* ps, std::vector<Vec3f>* ns) {
  assert(p1.x < int(grid.size.x) && p1.y < int(grid.size.y) && p1.z < int(grid.size.z));
  assert(all(gt(p0, Vec3<int>{}))); //  need positive boundary for normal computation.

  for (int i = p0.x; i < p1.x; i++) {
    for (int j = p0.y; j < p1.y; j++) {
      for (int k = p0.z; k < p1.z; k++) {
        const Vec3f cube{float(i), float(j), float(k)};
        auto sample = sample_grid(cube, grid, gen_surface, thresh);
        auto tris = gen_tris(cube, grid, sample, params);
        if (tris.num_triangles > 0) {
          const size_t curr_size = ps->size();
          const size_t num_ps = tris.num_triangles * 3;
          ps->resize(ps->size() + num_ps);
          memcpy(ps->data() + curr_size, tris.positions, sizeof(Vec3f) * num_ps);
#if GROVE_CUBE_MARCH_INCLUDE_NORMALS
          ns->resize(ns->size() + num_ps);
          memcpy(ns->data() + curr_size, tris.normals, sizeof(Vec3f) * num_ps);
#else
          (void) ns;
#endif
        }
      }
    }
  }
}

